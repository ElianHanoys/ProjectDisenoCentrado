   private void buscarPorId(int userId) {
    String consultaSQL = "SELECT * FROM usuario WHERE user_id = ?";
    
    try {
        PreparedStatement ps = cn.prepareStatement(consultaSQL);
        ps.setInt(1, userId);
        ResultSet rs = ps.executeQuery();
        
        DefaultTableModel modelo = (DefaultTableModel) tbtUsuario.getModel();
        modelo.setRowCount(0);
        modelo.addColumn("user_id");
        modelo.addColumn("nombre");
        modelo.addColumn("Apellido");
        modelo.addColumn("correo");
        modelo.addColumn("telefono");
        
        if (rs.next()) {
            String[] data=new String [5];
            id.setText(rs.getString("user_id"));
            txtnombre.setText(rs.getString("nombre"));
            Apellido.setText(rs.getString("Apellido"));
            correo.setText(rs.getString("correo"));
            Telefono.setText(rs.getString("telefono"));
            
            modelo.addRow(data);
            
        } else {
            JOptionPane.showMessageDialog(rootPane, "Usuario no encontrado.");
        }
        
    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(rootPane, "Error en la consulta SQL: " + ex);
    }
}
    
    
